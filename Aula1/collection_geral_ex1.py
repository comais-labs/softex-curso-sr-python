"""
Coleções em Python:
- Listas: é uma coleção de elementos ordenada e **mutável**. Permite duplicação de elementos.
- Tuplas: é uma coleção de elementos ordenada e **imutável**. Permite duplicação de elementos.
- Sets: é uma coléção de elementos **não ordenada** e **não indexada**. Não permite duplicação de elementos.
- Dicionários: é uma coleção de elementos **não ordenada**, **mutável** e **indexada**. Não permite duplicação de elementos.
"""

# Listas: Definindo uma lista
lista = ["banana", "maçã", "uva", "abacaxi", "laranja", "melancia"]
print(lista)

# Tuplas: Definindo uma tupla
tupla = ("banana", "maçã", "uva", "abacaxi", "laranja", "melancia")
print(tupla)

# Sets: Definindo um set
set = {"banana", "maçã", "uva", "abacaxi", "laranja", "melancia"}
print(set)

# Dicionários: Definindo um dicionário
dicionario = {
    "nome": "João",
    "idade": 30,
    "cidade": "São Paulo"
}
print(dicionario)
