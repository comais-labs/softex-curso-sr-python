# Definindo variáveis

# Variável do tipo inteiro
num = 10
print(num)  # Saída: 10

# Variável do tipo float (números decimais)
pi_value = 3.14
print(pi_value)  # Saída: 3.14

# Variável do tipo string (texto)
greeting = "Hello, World!"
print(greeting)  # Saída: Hello, World!

# Variável do tipo booleano (True ou False)
is_python_fun = True
print(is_python_fun)  # Saída: True

# Variáveis podem ser usadas em operações matemáticas
num2 = num + 5
print(num2)  # Saída: 15

# Variáveis podem ser usadas para armazenar o resultado de operações com outras variáveis
length = 10
width = 5
area = length * width
print(area)  # Saída: 50

# Variáveis podem ser atualizadas
num = num + 1
print(num)  # Saída: 11

# Python também suporta atualização de variável de forma abreviada
num += 1
print(num)  # Saída: 12
