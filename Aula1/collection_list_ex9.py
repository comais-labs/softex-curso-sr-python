"""
Coleções em Python:
- Listas: Ordenando e invertendo uma lista
"""

# Listas: Definindo uma lista
lista = ["banana", "maçã", "uva", "abacaxi", "laranja", "melancia"]

# Listas: Ordenando uma lista
lista.sort()
print(lista)

# Listas: Ordenando uma lista
lista.sort(reverse=True)
print(lista)

# Listas: Invertendo uma lista
lista.reverse()
print(lista)
