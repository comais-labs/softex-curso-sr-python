# Dictionary: Definindo um dicionário
dict1 = {
    "marca": "Ford",
    "modelo": "Mustang",
    "ano": 1964
}

# Dictionary: Acessando os valores de um dicionário utilizando o método values()
x = dict1.values()
print(x)
