# Dictionary: Definindo um dicionário
dict1 = {
    "marca": "Ford",
    "modelo": "Mustang",
    "ano": 1964
}

# Dictionary: Acessando as chaves de um dicionário utilizando o método keys()
x = dict1.keys()
print(x)
