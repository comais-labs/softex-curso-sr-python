# Inteiros (int) - usados para números inteiros
num = 10
print(num)
print(type(num))  # Saída: <class 'int'>

# Números de ponto flutuante (float) - usados para números reais
# (com uma parte decimal)
pi = 3.14
print(pi)
print(type(pi))  # Saída: <class 'float'>

# Strings (str) - usados para texto
greeting = "Hello, World!"
print(greeting)
print(type(greeting))  # Saída: <class 'str'>

# Booleanos (bool) - usados para valores verdadeiros ou falsos
is_python_fun = True
print(is_python_fun)
print(type(is_python_fun))  # Saída: <class 'bool'>

# Listas (list) - usados para coleções ordenadas de valores
numbers = [1, 2, 3, 4, 5]
print(numbers)
print(type(numbers))  # Saída: <class 'list'>

# Tuplas (tuple) - semelhante às listas, mas imutáveis
coordinates = (10.0, 20.0)
print(coordinates)
print(type(coordinates))  # Saída: <class 'tuple'>

# Dicionários (dict) - usados para coleções de pares chave-valor
person = {"name": "Alice", "age": 25}
print(person)
print(type(person))  # Saída: <class 'dict'>

# Conjuntos (set) - usados para coleções desordenadas de valores únicos
unique_numbers = {1, 2, 2, 3, 4, 4, 4, 5}
print(unique_numbers)  # Saída: {1, 2, 3, 4, 5}
print(type(unique_numbers))  # Saída: <class 'set'>
