"""
Coleções em Python:
- Dictionary: Acessando um item do dicionário utilizando o método get().
"""

# Dictionary: Definindo um dicionário
dict1 = {
    "marca": "Ford",
    "modelo": "Mustang",
    "ano": 1964
}

# Dictionary: Acessando um item do dicionário utilizando o método get()
x = dict1.get("modelo")
print(x)
